"""
Operadores:

+  Adição
-  Subtração

*  Multiplicação
** Exponenciação

/  Divisão
// Retorna o valor inteiro da divisão
%  Retorna o resto da divisão

>   Maior
<   Menor
>=  Maior ou igual
<=  Menor ou igual
==  Igual
!=  Diferente
=   Atribuição

"""

a = 2
b = 3

print(a + b)  # adição
print(a - b)  # subtrair

print(a * b)  # multiplicar
print(a ** b)  # exp

print(a / b)  # divisão
print(a // b)  # divisão com valor inteiro


